// armazena um valor na memória
// Const é a declaração mais recomendada para uso nas variáveis que não pode ter o valor alterado
// Não ser reatribuida nem redeclarada
// deve ser inicializada com valor

const PI = 3.14;

console.log(`PI: ${PI}`);

const potency = Math.pow(PI, 2);

console.log(potency);

// const não pode ser reatribuida
// PI = 3.14 + potency // Assignment to constant variable